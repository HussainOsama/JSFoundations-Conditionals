// Define the numbers and the operation here
let firstNumber = 5
let secondNumber = 7
let operation = "*"

if ( operation == "+"){
    console.log(firstNumber + secondNumber);
}else if ( operation == "-"){
    console.log(firstNumber - secondNumber);
}else if ( operation == "*"){
    console.log(firstNumber * secondNumber);
}else if ( operation == "/"){
    console.log(firstNumber / secondNumber);
}




